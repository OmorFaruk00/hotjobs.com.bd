              <p>Have you ever thought of your resume as a personal marketing tool? If not, maybe it's time you do. When
                you’re looking for a new job, your resume is typically the first contact that potential employers have
                with you. Your goal is to use your resume to create a great first impression–not by lying, but by
                putting your best foot forward and showing people exactly what you can do. Get started by using our
                sure-fire tips.</p>

              <h4>Build your experience</h4>
              <p>You know that you have to have experience in order to get a job, and you have to have a job in order to
                get experience. There are, however, some other ways you can get some great experience that’s relevant to
                your job. In the hospitality and restaurant industry, this might include:

              <ul>
                <li>Volunteering for events that require a great deal of customer service</li>
                <li>Finding opportunities to cook for friends, loved ones, and other events–especially if you get paid
                  to do it
                </li>
                <li>Organizing local events, including those that aren’t for profit</li>
              </ul>
              </p>


              <h4>Keep It Simple</h4>

              <ul>
                <li>Make certain the job title you use is easily understandable by your reader (for example: Tax
                  Accountant vs. Accounting Specialist II )
                </li>
                <li>Don't bother using obscure vocabulary, select familiar words.</li>
                <li>Utilize bullet points with short phrases.</li>
              </ul>

              <h4>Follow the Right Form</h4>
              <ul>
                <li>Instead of complete sentences, use short action phrases to list your accomplishments.</li>
                <li>Avoid repeating the same action verb.</li>
                <li>When describing a current position, use the present tense and vice versa when describing a past
                  one.
                </li>
                <li>Keep the length to one or two pages.</li>
              </ul>

              <h4>Personalize your resume. </h4>
              <p>Think about the specific job you’re applying for, then put relevant information first or emphasize it
                more clearly. For example, if you’re applying for a hospitality position, your time as a customer
                service representative at a call center might showcase your ability to prioritize customer service and
                handle high-stress situations. On the other hand, if you’re applying for a job as a restaurant manager,
                you might be better suited to discuss your previous experience in the restaurant industry at the top of
                your resume.</p>

              <h4>List accomplishments, not skills.</h4>
              <p>It’s great that you have customer service skills–but what have you done with them? Instead of a rote
                list of your skills, use active language to describe what you’ve actually accomplished with them. Which
                sounds more appealing: “good at dealing with customers” or “effectively managed customer interactions
                and diffused tense situations” as you describe your previous management position?</p>


              <h4>Keep it real</h4>
              <p>Don’t lie on your resume. This seems obvious, but all too many prospective employees fall prey to the
                urge to exaggerate or even manufacture information about their previous positions. If you want the job,
                be honest: chances are, someone will check before you’re offered the position. Lies will make it evident
                that you aren’t a suitable candidate.</p>

              <h4>Make It Look Good</h4>
              <ul>
                <li>Use standard 8-1/2 x 11 inch paper.</li>
                <li>Don't forget to include matching envelopes.</li>
                <li>Typeface should be easy-to-read (Courier, Times New Roman or Arial), so avoid the fancy script-like
                  fonts. Stick to black ink.
                </li>
                <li>Font size should be between 10-12 pts.</li>
                <li>Highlight section headers with boldface type.</li>
                <li>Be consistent by single spacing for individual sentences and double spacing between sections and
                  paragraphs.
                </li>
                <li>Make sure your margins have at least one inch of white space on the top and no less than one
                  half-inch on the other three sides.
                </li>
                <li>When printing your resume, look for a watermark. Hold your paper up to the light and you'll
                  typically see the brand of the paper. Make sure the mark is right side up when you print.
                </li>
                <li>Consider sending your resume in a 9x12 inch envelope so it doesn't have to be folded.</li>
              </ul>


