              <h5>Tips for Creating an Effective Resume</h5>
              <p>When an employer looks at your resume, you've got about 30 seconds to make a
                good impression. That's the average amount of time most employers spend
                reading a resume. To capture the reader's attention, you need to be clear and tothe-point. Keep in mind
                the following guidelines to create an effective resume:</p>

              <h5>Be Concise and Omit Irrelevant Information</h5>
              <p>Employers don't want to read a long, drawn-out version of your life's accomplishments. They have
                stacks of resumes to read, and want to know quickly whether or not you would be a good fit for
                their company. So, be concise, and exhibit your skills and abilities.
                A resume should only include information that will help convince an employer to interview you.
                Descriptions of relevant skills and accomplishments should be concise and to the point. Only include
                personal information where it demonstrates an important personal quality or qualification. A
                resume should represent what you can do on the job, not what you do in your personal life.</p>

              <h5>Don't Forget Your Objective</h5>
              <p>Before diving into writing your resume, plan out your career objective. Make it clear and focused.
                Remember to keep the message consistent throughout your resume as you summarize your skills
                and accentuate your strengths.</p>

              <h5>Write a Powerful Opening Statement</h5>
              <p>If you are a recent graduate or have limited experience in your career, you should include a Job
                Objective statement at the top of your resume, which will help focus the reader's attention and
                describe what type of position you are looking for. If you have experience in your career field, you
                want a powerful Summary statement that illustrates your best qualifications for the position at the
                top of the resume. A well-crafted opening statement should convince an employer to keep reading.</p>

              <h5>Customize It: Focus on Your Benefit to Employers</h5>
              <p>Describe your experience in a way that relates to the skills the employer is seeking. Focus on
                highlighting accomplishments that will arouse the interest of employers who read your resume.
                Answer the question: "How can this candidate fulfill the role and make a positive impact?" If a
                company is looking for a candidate who has leadership ability, highlight your experience in training
                new employees or in managing projects. Remember that the goal is to get the interview.</p>

              <h5>Include Keywords</h5>
              <p>Draw attention to your resume with keywords that describe your skills and experience. Inserting
                descriptive keywords in your resume is becoming more important as an increasing number of
                companies search for resumes in computer databases that find matches through keywords. These
                words should relate to the job you are applying for while highlighting your own skill set.</p>

              <h5>Use Action-Benefit statements: Avoid Boring List of Job
                Responsibilities</h5>
              <p>The best resumes describe experience using Action-Benefit statements, which describe an action
                you took in response to a challenge or opportunity, and explain how your action had a positive
                benefit for your company. This method brings your experience statements to life and demonstrates
                how you can achieve success and produce results. An Action-Benefit statement might read
                "Analyzed declining sales and developed campaign that increased orders by 30% in less than one
                month." This statement describes the situation or challenges you faced (declining sales), the Action
                you took (developed a campaign), and the Benefit of your actions (a 30% increase in orders).</p>

              <h5>Quantify Your Achievements</h5>
              <p>You need to give the reader an idea of what you have done throughout your career, but instead of
                focusing on the duties you were responsible for at your last jobs, list your accomplishments in
                action-benefit statements along with quantifiable facts to back up your claims. Use numbers,
                percentages and dollar amounts to show your success in achieving company goals. Instead of
                writing "Responsible for increasing sales in my territory," use "Increased sales in my territory 150%
                over 6 months. </p>

              <h5>Use Action Verbs</h5>
              <p>Portray yourself as active, accomplished, intelligent, and capable of contributing. Examples:
                Managed, Launched, Created, Directed, Established, Organized, and Supervised. Never use the
                same action word repeatedly. Instead of using a word like 'directed' over and over, use synonyms
                such as controlled, supervised, guided, or managed. Utilize a thesaurus, career advice Web sites and
                other sources if you are having problems coming up with new ways to say the same thing.

              <h5>Be Professional, Not Personal</h5>
              <p>You do not have much room in a resume, so why take up valuable space with information unrelated
                to the position you are seeking? Focus on your work experiences and never refer to personal
                information such as race, religion, marital status, age, political party, or even personal views. In all
                but a few instances, it would be illegal for the employer to consider such issues. Also, avoid the use
                of humor and clichés in resumes and forget about mentioning the fact that you enjoy horror movies
                and surfing in your spare time</p>

              <h5>Personal Traits</h5>
              <p>If you want to include personal traits in your resume, such as "Dependable, Highly-Organized,
                SelfMotivated, and Responsible," rather than just listing these traits, try demonstrating these
                characteristics using examples from your experience. For example, instead of writing "Dependable,"
                write "Never missed an important deadline in five years as a project manager."</p>

              <h5>Keep a Positive Tone </h5>
              <p>

                Your resume should present you in the best possible light. If you don't possess every skill an
                employer is seeking, do not emphasize your shortcomings. Focus on what you can offer.
              </p>

              <h5>Be Honest</h5>

              <p>Be honest about your skills and abilities. Never exaggerate your abilities.</p>

              <h5>Be Organized and Logical</h5>
              <p>In addition to reviewing your experience, employers also use the resume to sense whether you are
                organized, logical, and concise. Make sure your resume is balanced, neat, visually appealing, and
                flows consistently. Clearly separate sections and emphasize section titles.</p>

              <h5>Just Communicate</h5>
              <p>Abandon the use of exorbitant, exquisite vocabulary. In other words, don't try to impress employers
                with the depth of your vocabulary. Use words everyone can understand in a simple way.</p>
              <h5>Combine Sections When Possible</h5>
              <p>
                Try to combine any short sections together to make your resume more compact. For example, if
                you only have one entry under training, consider placing it under your education instead and
                change the section title to "Education and Training."
              </p>

              <h5>Use Common Section Headings</h5>
              <p>Use common section headings. Examples: Objective, Experience, Employment, Work History, Skills,
                Summary, Summary of Qualifications, Accomplishments, Achievements, Capabilities, Education,
                Professional Affiliations, Publications, Licenses and Certifications, and Honors.</p>

              <h5>Omit Salary Information</h5>
              <p>Never refer to salary in your resume. Save this information for the interview.</p>

              <h5>Avoid Writing in First Person ("me" or "I")</h5>

              <p>Your resume is not a personal correspondence, and should not include words such as "I," "my," or
                "me." Replace such personal pronouns with short action statements. Instead of saying, "I wrote
                several articles for the company newsletter," try, "Researched and wrote several articles for the
                company newsletter." Save the first person pronouns for your cover letter.</p>

              <h5>Placing Professional Titles</h5>
              <p>There are several places where it would be appropriate to put your professional title. You can place
                your title next to your name in the format "Ahmed Abdullah, FCA." You can include it in your
                Summary, Education, or Licenses/Certifications sections. If your title is an important qualification in
                your job search, be sure it appears at the top of your resume.</p>
              <h5>Including References in Your Resume</h5>
              <p>You do not have to include references on your resume. Most employers will assume you can
                provide references if they are requested.</p>

              <h4>Watch out for Exteriors of Resume</h4>

              <h5>Resume Length</h5>
              <p>A resume should be as long as needed to list your best and most relevant qualifications for the job
                you are seeking. For recent graduates or those with only a few years of experience, you should be
                able to put all your relevant experience on a single page. If you have extensive experience in your
                field, you may require a two-page resume to list all or your relevant experience. Instead of
                considering the length of your resume, make sure that it is clear and concise, and that the
                information is relevant to the position you are seeking. The most important consideration for a
                resume is not length, but whether it sufficiently describes your best qualifications for the job.</p>

              <h5>Paper Size</h5>
              <p>Use standard A4 or letter (8½" x 11") size offset paper. Recruiters handle hundreds of resumes; if
                yours is on a smaller sheet, it is likely to be lost in the pile, and if it is oversized, it may get crumpled
                and have trouble fitting in a company's file.</p>

              <h5>Paper Color</h5>
              <p>White and ivory are the only Paper colors considered acceptable for resumes and cover letters.</p>

              <h5>Computers & Word Processing</h5>
              <p>The most flexible way to type your resume is on a computer or word processor. This allows you to
                make changes almost instantly, and to store different drafts on disk. Word processing systems also
                give you many different options such as bold facing for emphasis, different fonts or typefaces and
                justified margins.</p>

              <h5>Printing Your Resume</h5>
              <p>Find the best quality offset printing process available and do not make your copies on a
                photocopier. Only the personnel office may see the resume you mail; everyone else may see a copy
                of it. Copies of copies quickly become illegible.</p>

              <h5>Proofreading is Essential</h5>
              <p>Mistakes on resumes can be embarrassing and no doubt you are the only one who will lose if there
                is a mistake. So proofread your resume as carefully as possible and then have a friend do it. Make
                sure your spelling and grammar are perfect. Don't rely entirely on computer or word-processors
                built-in spell-check programs, because not all typos are misspellings and a spelling checker is not a
                substitute for proofreading for your resume. Words spelled correctly can still be used improperly.
                Bear in mind that a spelling checker cannot flag errors such as "to" for "two" or "bills" for "skills."</p>

              <h5>Utilize Bullet-Points</h5>
              <p>Use bullet points in the body of your resume instead of long paragraphs. Resumes are read rather
                quickly and anything you can do to draw the reader in will bring you one step closer to landing an
                interview.</p>

              <h5>Consistent Formatting</h5>
              <p>Choose easy-to-read fonts and use consistent formatting for headings and bullet points. Also, make
                sure you leave enough space between each section with boldface or capital lettering. Use bold,
                italics, and underlining to highlight the most relevant information on your resume. In the same
                respect, steer clear of flashy formatting or overly creative resumes with unconventional fonts or
                graphics, unless you are seeking a highly creative position. Keep your resume simple, bold and
                professional.</p>

              <h5>Avoid Overuse of Highlighting: Bold / Italics / Underline</h5>
              <p>Drawing attention to everything is the same as drawing attention to nothing. Use highlighting such
                as bold, italics, and underline sparingly for maximum effect on specific areas you want to draw
                attention to and be consistent in your highlighting technique</p>

