              <h4>NGO পরিচিতি</h4>

              <p>NGO (Non-Government Organization) হল বেসরকারি অলাভজনক সংগঠন যারা দেশ, সমাজ ও মানুষের উন্নয়নের জন্য
                নানামুখী উন্নয়নমূলক এবং কল্যানমূলক কাজ করে থাকে। এদের উদ্দেশ্য হচ্ছে বিভিন্ন বিদেশি দাতাদের অর্থায়নের
                ভিত্তিতে সরকারকে উন্নয়নে সহযোগীতা করা ও আর্থ-সামাজিক উন্নয়নে সক্রিয় অবদান রাখা। বাংলাদেশে বর্তমানে
                দুই হাজারের বেশি এনজিও আছে, এরা শিক্ষা, স্বাস্থ্য, কৃষি, কর্মসংস্থান ও অন্যান্য বিষয় নিয়ে কাজ
