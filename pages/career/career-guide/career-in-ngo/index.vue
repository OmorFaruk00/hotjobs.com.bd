                শহর ও গ্রাম উভয় এলাকাতেই কর্মপরিচালনা করে।
                এনজিওগুলোর কাজ বিশেষ ভৌগোলিক এলাকা ভিত্তিক-ও হয়ে থাকে, যেমন চর/উপকূলীয়/পার্বত্য এলাকা কেন্দ্রীক এনজিও।
