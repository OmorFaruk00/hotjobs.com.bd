                তাদের সমস্যার কথা সরকার ও সমাজের প্রতিষ্ঠিত মহলকে জানানো, তাদের উন্নয়নে বিভিন্ন প্রজেক্ট হাতে নেওয়া,
                এর জন্য দাতাগোষ্ঠীর কাছ থেকে অনুদান সংগ্রহ করা, সামগ্রিকভাবে বিভিন্ন প্রজেক্ট পরিচালনা করা- নানা স্তরে
