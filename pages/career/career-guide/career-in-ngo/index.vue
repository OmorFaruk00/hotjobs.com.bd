                কল্যাণে কাজ করতে হয় স্বতঃস্ফূর্তভাবে। তাই এনজিওতে আপনি কেন কাজ করবেন তা নির্ভর করছে আপনার সমাজের সুবিধা
                বঞ্চিত মানুষের পাশে দাঁড়ানোর ইচ্ছা থেকে। এছাড়াও নিজের ক্যারিয়ার গড়ার সহায়ক উপাদান হিসেবে এনজিওতে
                কর্মরত অবস্থায় আপনি বিভিন্ন ধরণের সুযোগ সুবিধা পেয়ে থাকবেন। দেশের বিভিন্ন স্থানে ভ্রমণের সুযোগ থেকে
                আপনি সে সকল অঞ্চলের মানুষ এবং সংস্কৃতি সম্পর্কে জানতে পারবেন একেবারে কাছ থেকে।</p>

              <p>কাজের ধরন: এনজিওতে কাজের ধরনকে প্রধানত দুটি ভাগে ভাগ করা যায়। একটি হচ্ছে প্রজেক্ট ভিত্তিক, এবং আরেকটি
