                দেশি এনজিওগুলোর তুলনায় অপেক্ষাকৃত বেশি বেতন দিয়ে থাকে। এন্ট্রি লেভেলের কর্মীদের বেতন ১০ হাজার থেকে ৩৫
                হাজার টাকার মধ্যে হয়ে থাকে। কর্মীকে কোন্‌ এলাকায় নিয়োগ দেওয়া হয়েছে তার উপরেও অনেক সময় বেতনের
