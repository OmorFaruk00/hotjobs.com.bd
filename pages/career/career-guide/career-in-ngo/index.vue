                সরাসরি সম্পৃক্ত থাকে না। এগুলোকে সুপারভাইজরি পজিশন-ও বলা হয়ে থাকে। যেমন, ফিন্যান্স ম্যানেজার, একাউন্টস
                ম্যানেজার, হিউম্যান রিসোর্স ম্যানেজার ইত্যাদি। স্থায়ী ম্যানেজারদের বেতন-ভাতাদি নির্দিষ্ট কোনো
