                নেয়। এসব পদ সাধারণত যারা মাস্টার্স শেষ করেছে, তাদের জন্য খোলা থাকে। আবার এস.এস.সি ও এইচ.এস পাশ করে যারা
                কাজ শুরু করতে চান, তাদের জন্য কিছু পদ রয়েছে, যেমন- Facilitator, Community Facilitator, Community
                Teacher, Community Mobilizer ইত্যাদি। এদের বেতন কাঠামো ৬ হাজার থেকে ১০ হাজার টাকার মধ্যে সীমাবদ্ধ হয়ে
